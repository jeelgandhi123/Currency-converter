const fromSelect = document.getElementById("from-currency");
const toSelect = document.getElementById("to-currency");
const fromFlag = document.getElementById("from-flag");
const toFlag = document.getElementById("to-flag");
const form = document.getElementById("converter-form");
const amountInput = document.getElementById("amount");
const exchangeMsg = document.getElementById("exchange-rate");

// Populate select options
Object.keys(currencyList).forEach((code) => {
  const option1 = new Option(code, code);
  const option2 = new Option(code, code);
  fromSelect.add(option1.cloneNode(true));
  toSelect.add(option2.cloneNode(true));
});
fromSelect.value = "USD";
toSelect.value = "INR";

// Update flags on currency change
fromSelect.addEventListener("change", () => {
  fromFlag.src = `https://flagsapi.com/${currencyList[fromSelect.value]}/flat/64.png`;
});
toSelect.addEventListener("change", () => {
  toFlag.src = `https://flagsapi.com/${currencyList[toSelect.value]}/flat/64.png`;
});

// Fetch exchange rate
form.addEventListener("submit", async (e) => {
  e.preventDefault();

  const amount = parseFloat(amountInput.value);
  const from = fromSelect.value;
  const to = toSelect.value;

  if (isNaN(amount) || amount <= 0) {
    alert("Enter a valid amount!");
    return;
  }

  try {
    const res = await fetch(`https://api.exchangerate-api.com/v4/latest/${from}`);
    const data = await res.json();
    const rate = data.rates[to];
    const converted = (amount * rate).toFixed(2);

    exchangeMsg.innerText = `${amount} ${from} = ${converted} ${to}`;
  } catch (error) {
    exchangeMsg.innerText = "Something went wrong. Try again later.";
  }
});
